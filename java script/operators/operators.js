 var ss=263;
 var ssk=++ss;
 console.log(ss);
 console.log("pre increment is",ssk); 

 //post decrement
 var ss=263;
 var ssk=ss--;
 console.log(ss);
 console.log("post decrement is",ssk); 

 //pre decrement
 var ss=263;
 var ssk=--ss;
 console.log(ss);
 console.log("pre decrement is",ssk); 

